package jorge.movies.solid;

import java.util.Enumeration;
import java.util.Vector;

public class Customer {
    private String _name;
    private Vector _rentals = new Vector();

    public Customer(String name) {
        _name = name;
    }

    public void addRental(AbstractRentalMovie arg) {
        _rentals.addElement(arg);
    }

    public String getName() {
        return _name;
    }

    public String statement() {
        double totalAmount = 0;
        int frequentRenterPoints = 0;
        Enumeration rentals = _rentals.elements();
        String result = "Rental Record for " + getName() + "\n";
        while (rentals.hasMoreElements()) {

            AbstractRentalMovie each = (AbstractRentalMovie) rentals.nextElement();
            double thisAmount = each.getRentalAmount();

            // add frequent renter points for Children and New Release
            // keep in mind Regular movies don't have frequent renter points
            if (each.isNewReleaseCategory() || each.isChildrenCategory()) {
                frequentRenterPoints++;
            }
            // add bonus for a two day new release rental
            if (each.isNewReleaseCategory() && each.getDaysRented() > 1) {
                frequentRenterPoints++;
            }
            //show figures for this rental
            result += "\t" + each.getMovie().getTitle() + "\t" +
                    String.valueOf(thisAmount) + "\n";
            totalAmount += thisAmount;
        }
        //add footer lines
        result += "Amount owed is " + String.valueOf(totalAmount) +
                "\n";
        result += "You earned " + String.valueOf(frequentRenterPoints)
                +
                " frequent renter points";
        return result;
    }
}
